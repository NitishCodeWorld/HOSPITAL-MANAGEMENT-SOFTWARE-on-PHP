function ShowMenu(){
    if(document.getElementById('submenu').style.display == 'block'){
        document.getElementById('submenu').style.display = 'none';
        document.getElementById('drop').className = 'fas fa-angle-left';
    }else{
        document.getElementById('submenu').style.display = 'block';
        document.getElementById('drop').className = 'fas fa-angle-down';
        document.getElementById('submenu1').style.display = 'none';
        document.getElementById('drop1').className = 'fas fa-angle-left';
        document.getElementById('submenu2').style.display = 'none';
        document.getElementById('drop2').className = 'fas fa-angle-left';
        document.getElementById('submenu3').style.display = 'none';
        document.getElementById('drop3').className = 'fas fa-angle-left';
        document.getElementById('submenu4').style.display = 'none';
        document.getElementById('drop4').className = 'fas fa-angle-left';
        document.getElementById('submenu5').style.display = 'none';
        document.getElementById('drop5').className = 'fas fa-angle-left';
    }
}
